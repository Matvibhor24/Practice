#include <iostream>

using namespace std;

int main(){
    int num1,num2;
    // "<<" is insertion operator.
    //">>" is extraction operator.
    cout<<"Enter first value : ";
    cin>>num1;
    cout<<"\nEnter second value : ";
    cin>>num2;
    cout<<"\nThe sum of "<<num1<<" and "<<num2<<" is : "<<num1+num2;
    return 0;
}
